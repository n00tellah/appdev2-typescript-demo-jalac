"use strict";
(() => {
    // Special Type - null
    let a;
    a = null;
    // a = "Hi"
    // b = "Noots"
    let b;
    b = null;
    b = "Hi";
    b = "Noots";
    // b = 10
    // Special Type - undefined
    let c;
    c = undefined;
    // c = null
    // c = "Hello"
    let d;
    d = undefined;
    d = "Hi";
    d = "Noots";
    d = 10;
    // d = null
    console.log(a);
    console.log(b);
    console.log(c);
    console.log(d);
})();
